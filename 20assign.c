//20. WAP to perform the post increment and post decrement operator on two integers and print both original value and updated value.

#include <stdio.h>
int main() 
{
    int num1 = 10;
    int num2 = 5;
    printf("\nSPARSH TANEJA 2316012133");
    printf("\nOriginal value of num1: %d\n", num1);
    printf("Post-incrementing num1: %d\n", num1++);
    printf("Post-decrementing num1: %d\n", num1--); 
    printf("Original value of num2: %d\n", num2);
    printf("Post-incrementing num2: %d\n", num2++);
    printf("Post-decrementing num2: %d\n", num2--); 
    return 0;
}